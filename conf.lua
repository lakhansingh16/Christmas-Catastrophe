function love.conf(t)
    t.title = 'Christmas Catastrophe!'               --title of the window
    t.version = '11.3'                               --Love2d version used
    t.console = true                                 --console window attached
    t.window.height = 720                            --window height
    t.window.width = 1280                            --window width
    t.window.vsync = true
end